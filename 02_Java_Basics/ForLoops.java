public class ForLoops {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }

        System.out.println();

        int[] arr = {3, 2, 4, 1};
        for (int num : arr) {
            System.out.println(num);
        }

    }
}
