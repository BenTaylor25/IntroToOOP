package ClassesAndObjects;

public class DogNoClass {
    public static void main(String[] args) {

        String myDogName = "Benji";
        int myDogAge = 5;
        String myDogBreed = "Golden Retriever";

        String yourDogName = "Bailey";
        int yourDogAge = 10;
        String yourDogBreed = "Dachshund";

        String theirDogName = "Bailey";
        int theirDogAge = 10;
        String theirDogBreed = "Dachshund";

        System.out.println(myDogName + ", " + myDogAge + ", " + myDogBreed);
        System.out.println(yourDogName + ", " + yourDogAge + ", " + yourDogBreed);
        System.out.println(theirDogName + ", " + theirDogAge + ", " + theirDogBreed);

    }
}
